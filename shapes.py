#This will print the star startint with 1 star on line one, 10 stars on line ten,
#and decreasing back to 1 star at line 20
star = 0
while star != 10:
        print('*'* star)
        star +=1
while star != 0:
        print('*'* star)
        star -=1

        

                        

        
               
